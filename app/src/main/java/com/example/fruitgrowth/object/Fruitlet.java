package com.example.fruitgrowth.object;

public class Fruitlet {

    Integer treeNumber;
    Integer clusterNumber;
    Integer fruitletNumber;
    Float fruitletSize;

    public Fruitlet(Integer treeNumber, Integer clusterNumber, Integer fruitletNumber, Float fruitletSize) {
        this.treeNumber = treeNumber;
        this.clusterNumber = clusterNumber;
        this.fruitletNumber = fruitletNumber;
        this.fruitletSize = fruitletSize;
    }

}
